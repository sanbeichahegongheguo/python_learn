a, b = 2, 3
print(a, "+", b, "=", a + b)  # 加
print(a, "-", b, "=", a - b)  # 减
print(a, "*", b, "=", a * b)  # 乘
print(a, "/", b, "=", a / b)  # 除
print(a, "%", b, "=", a % b)  # 取余
print(a, "**", b, "=", a ** b)  # 幂
print(a, "//", b, "=", a // b)  # 整除

# 注意：python不支持自增运算符和自减运算符。例如i++/i-是错误的，但i+=1是可以的。
